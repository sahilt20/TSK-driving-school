#!/usr/bin/env node

/**
 * Interactive Supabase Setup Helper
 *
 * This script helps you configure Supabase for the Cricket Club Platform
 *
 * Usage:
 *   node setup-supabase.js
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

function ask(question) {
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      resolve(answer.trim());
    });
  });
}

function printHeader() {
  console.log('\n' + '='.repeat(60));
  console.log('🏏 Cricket Club Platform - Supabase Setup Helper');
  console.log('='.repeat(60) + '\n');
}

function printStep(step, title) {
  console.log(`\n📍 Step ${step}: ${title}`);
  console.log('-'.repeat(60));
}

async function main() {
  printHeader();

  console.log('This script will help you configure Supabase.\n');
  console.log('You will need:');
  console.log('  1. A Supabase account (free)');
  console.log('  2. A Supabase project');
  console.log('  3. Your project URL and API keys\n');

  const hasAccount = await ask('Do you already have a Supabase account? (y/n): ');

  if (hasAccount.toLowerCase() !== 'y') {
    printStep(1, 'Create Supabase Account');
    console.log('\n1. Open your browser and go to: https://supabase.com');
    console.log('2. Click "Start your project"');
    console.log('3. Sign up with GitHub, Google, or Email');
    console.log('4. Once signed in, create a new project');
    console.log('5. Wait 2-3 minutes for project initialization\n');

    await ask('Press Enter once your project is ready...');
  }

  printStep(2, 'Get Your Credentials');
  console.log('\nIn your Supabase dashboard:');
  console.log('1. Click "Settings" (gear icon) in the left sidebar');
  console.log('2. Click "API"');
  console.log('3. You\'ll see "Project URL" and "Project API keys"\n');

  const projectUrl = await ask('Enter your Project URL (https://xxxxx.supabase.co): ');

  if (!projectUrl.startsWith('https://') || !projectUrl.includes('supabase.co')) {
    console.log('\n❌ Invalid URL format. It should look like: https://abcdefgh.supabase.co');
    console.log('Please restart the script and try again.');
    rl.close();
    return;
  }

  const anonKey = await ask('\nEnter your anon/public key (starts with eyJ...): ');

  if (!anonKey.startsWith('eyJ')) {
    console.log('\n⚠️  Warning: API key should start with "eyJ"');
    console.log('Make sure you copied the "anon public" key, not the service_role key.');
  }

  const wantServiceKey = await ask('\nDo you want to add the service_role key now? (optional, y/n): ');
  let serviceKey = '';

  if (wantServiceKey.toLowerCase() === 'y') {
    serviceKey = await ask('Enter your service_role key: ');
  }

  printStep(3, 'Update .env.local');

  const envPath = path.join(__dirname, '.env.local');
  const envContent = `# Supabase Configuration
# Get these values from https://app.supabase.com/project/_/settings/api

NEXT_PUBLIC_SUPABASE_URL=${projectUrl}
NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}
${serviceKey ? `SUPABASE_SERVICE_ROLE_KEY=${serviceKey}` : '# SUPABASE_SERVICE_ROLE_KEY=your-service-role-key-here'}

# App Configuration
NEXT_PUBLIC_APP_URL=http://localhost:3000

# Generated by setup-supabase.js on ${new Date().toISOString()}
`;

  try {
    fs.writeFileSync(envPath, envContent);
    console.log('\n✅ Successfully updated .env.local');
  } catch (error) {
    console.log('\n❌ Error writing .env.local:', error.message);
    console.log('\nPlease manually update .env.local with these values:');
    console.log(envContent);
    rl.close();
    return;
  }

  printStep(4, 'Setup Database Schema');
  console.log('\nNow you need to run the database schema in Supabase:\n');
  console.log('1. In Supabase, click "SQL Editor" in the left sidebar');
  console.log('2. Click "New query"');
  console.log('3. Open the file: supabase/schema.sql in this project');
  console.log('4. Copy ALL the content');
  console.log('5. Paste into Supabase SQL Editor');
  console.log('6. Click "Run" or press Ctrl+Enter');
  console.log('7. Wait for "Success. No rows returned" message\n');

  await ask('Press Enter once you\'ve run the schema...');

  printStep(5, 'Verify Setup');
  console.log('\nLet\'s verify the setup:\n');
  console.log('1. In Supabase, click "Table Editor"');
  console.log('2. You should see these tables:');
  console.log('   - teams');
  console.log('   - players');
  console.log('   - matches');
  console.log('   - innings');
  console.log('   - balls');
  console.log('   - batting_performances');
  console.log('   - bowling_performances');
  console.log('   - partnerships');
  console.log('   - live_match_state\n');

  const tablesCreated = await ask('Do you see all these tables? (y/n): ');

  if (tablesCreated.toLowerCase() !== 'y') {
    console.log('\n⚠️  Tables not found. Please:');
    console.log('1. Check for errors in the SQL Editor');
    console.log('2. Make sure you copied the ENTIRE schema.sql file');
    console.log('3. Refresh the Table Editor page');
    console.log('4. If still issues, see SUPABASE_SETUP_COMPLETE.md for troubleshooting\n');
  } else {
    console.log('\n✅ Database setup complete!');
  }

  printStep(6, 'Test Connection');
  console.log('\nFinal step - test the connection:\n');
  console.log('1. Open a new terminal');
  console.log('2. Run: npm run dev');
  console.log('3. Open: http://localhost:3000/matches');
  console.log('4. Click "New Team"');
  console.log('5. Create a test team (e.g., "Mumbai Indians", "MI")');
  console.log('6. If successful, the team will appear!\n');

  console.log('='.repeat(60));
  console.log('🎉 Setup Complete!');
  console.log('='.repeat(60));
  console.log('\nYour configuration:');
  console.log(`  Supabase URL: ${projectUrl}`);
  console.log(`  Anon Key: ${anonKey.substring(0, 20)}...`);
  console.log(`  Service Key: ${serviceKey ? serviceKey.substring(0, 20) + '...' : 'Not configured'}`);
  console.log('\nNext steps:');
  console.log('  1. Restart your dev server (npm run dev)');
  console.log('  2. Try creating a team');
  console.log('  3. Read SUPABASE_SETUP_COMPLETE.md for more details\n');
  console.log('Need help? Check the troubleshooting section in SUPABASE_SETUP_COMPLETE.md\n');

  rl.close();
}

main().catch((error) => {
  console.error('\n❌ Error:', error.message);
  rl.close();
  process.exit(1);
});
